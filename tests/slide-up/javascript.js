$( document ).ready( function() {
    var button = $('.button');
    var slider = $('.slider');
    button.on('click', function(e) {
        if ( slider.hasClass('closed') ) {
            slider.toggleClass('closed');
        } else {
            slider.toggleClass('closed');
        }
    });
});

